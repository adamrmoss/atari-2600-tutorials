; ╔══════════════════════════════════════════════════════════════════════════╗
; ║ THROW message                                                            ║
; ╠══════════════════════════════════════════════════════════════════════════╣
; ║ Stop the assembly process and write message to console                   ║
; ╚══════════════════════════════════════════════════════════════════════════╝
    mac THROW
        echo {1}
        err
    endm

; ╔══════════════════════════════════════════════════════════════════════════╗
; ║ THROW2 message, message                                                  ║
; ╠══════════════════════════════════════════════════════════════════════════╣
; ║ Stop the assembly process and write two messages to console              ║
; ╚══════════════════════════════════════════════════════════════════════════╝
    mac THROW2
        echo {1}, {2}
        err
    endm

; ╔══════════════════════════════════════════════════════════════════════════╗
; ║ LOG message                                                              ║
; ╠══════════════════════════════════════════════════════════════════════════╣
; ║ If verbosity enabled, write message to console                           ║
; ╚══════════════════════════════════════════════════════════════════════════╝
    mac LOG
        ifconst VERBOSE
            echo {1}
        endif
    endm

; ╔══════════════════════════════════════════════════════════════════════════╗
; ║ LOG2 message, message                                                    ║
; ╠══════════════════════════════════════════════════════════════════════════╣
; ║ If verbosity enabled, write two messages to console                      ║
; ╚══════════════════════════════════════════════════════════════════════════╝
    mac LOG2
        ifconst VERBOSE
            echo {1}, {2}
        endif
    endm

; ╔══════════════════════════════════════════════════════════════════════════╗
; ║ LOG_CR                                                                   ║
; ╠══════════════════════════════════════════════════════════════════════════╣
; ║ If verbosity enabled, echo a carriage return                             ║
; ╚══════════════════════════════════════════════════════════════════════════╝
    mac LOG_CR
        ifconst VERBOSE
            echo ""
        endif
    endm
